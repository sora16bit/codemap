const DICTS: Record<Lang, Dict> = { en, ja, "zh-CN": zhCN, es, fr, de, pt, ko };

/** ブラウザの言語設定から初期言語を選ぶ。未対応なら英語。 */
export function detectLang(): Lang {
  if (typeof navigator === "undefined") return "en";
  for (const pref of navigator.languages ?? [navigator.language]) {
    const lower = pref.toLowerCase();
    if (lower.startsWith("zh")) return "zh-CN";
    const two = lower.slice(0, 2);
    const hit = LANGS.find((l) => l.toLowerCase().startsWith(two));
    if (hit) return hit;
  }
  return "en";
}

/**
 * 翻訳を引く。キーが無ければ英語→キー文字列にフォールバック。
 * params で {count} などを差し込む。
 */
export function t(
  lang: Lang,
  key: string,
  params?: Record<string, string | number>,
): string {
  let s = DICTS[lang]?.[key] ?? en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}
